//学校地区初始化
function InitCollegeCity(objLevel1){
    for (var iCount = 0; iCount < intCollegeCityCount; iCount++)
        objLevel1.options[iCount+1] = new Option (arrCollegeCity[iCount][1],arrCollegeCity[iCount][0]);
}

//学校初始化
function InitCollege(objLevel1,objLevel2,itmSelected){
    if (itmSelected==''){
		objLevel2.options[0].selected = true;
	}
	else{
    for (var iCount = 0; iCount < intCollegeCityCount; iCount++)
        for (var intIndex = 0; intIndex < arrCollegeID[iCount].length; intIndex++)
            if (itmSelected == arrCollegeID[iCount][intIndex]){
                objLevel1.options[iCount+1].selected = true;
                
                for (var i=0;i < arrCollegeID[iCount].length;i++)
                    objLevel2.options[i+1]=new Option(arrCollegeValue[iCount][i],arrCollegeID[iCount][i]);

                objLevel2.options[intIndex+1].selected = true;
            }
   }   
}

//二级下拉框联动，二级下拉框的内容根据一级下拉框选择的值不同而不同
function SelectCollegeCity(objLevel1,objLevel2){
  
    var intIndex=objLevel1.options.selectedIndex-1;
  
for (var iClear=objLevel2.options.length-1;iClear > 1;iClear--)
        objLevel2.options.remove(iClear);
 if(objLevel1.options[objLevel1.options.selectedIndex].value != ''){
    
    for (var iCount=0;iCount < arrCollegeID[intIndex].length;iCount++)
        objLevel2.options[iCount+1]=new Option(arrCollegeValue[intIndex][iCount],arrCollegeID[intIndex][iCount]);
    if (iCount > 1){
        objLevel2.options[0].selected=true; 
    }
    else{
        objLevel2.options[1].selected=true;
    }
   }
 else
  {
    for (var iCount=1;iCount < objLevel2.options.length;iCount++)
    objLevel2.options.remove(iCount);
  }
}

